import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const DATA_DIR = '/tmp/taiwan-flashcard/data';
const OUTPUT_FILE = path.join(__dirname, '..', 'src', 'data', 'vocab-index.js');

const files = [
  'vocab-novice1.json',
  'vocab-novice2.json',
  'vocab-level1.json',
  'vocab-level2.json',
  'vocab-level3.json',
  'vocab-level4.json',
  'vocab-level5.json',
];

const vocabMap = {};

for (const file of files) {
  const filePath = path.join(DATA_DIR, file);
  const entries = JSON.parse(fs.readFileSync(filePath, 'utf-8'));

  for (const entry of entries) {
    const { hanzi, pinyin, meaning_en, meaning_ja, part_of_speech } = entry;
    const value = { pinyin, meaning_en, meaning_ja, part_of_speech };

    if (hanzi.includes('/')) {
      const variants = hanzi.split('/');
      for (const variant of variants) {
        const trimmed = variant.trim();
        if (trimmed && !vocabMap[trimmed]) {
          vocabMap[trimmed] = value;
        }
      }
    } else {
      if (!vocabMap[hanzi]) {
        vocabMap[hanzi] = value;
      }
    }
  }
}

const keys = Object.keys(vocabMap);
const lines = keys.map((key) => {
  const v = vocabMap[key];
  const pinyin = JSON.stringify(v.pinyin);
  const meaning_en = JSON.stringify(v.meaning_en);
  const meaning_ja = JSON.stringify(v.meaning_ja);
  const pos = JSON.stringify(v.part_of_speech);
  return `  ${JSON.stringify(key)}: { pinyin: ${pinyin}, meaning_en: ${meaning_en}, meaning_ja: ${meaning_ja}, part_of_speech: ${pos} }`;
});

const output = `// Auto-generated by scripts/build-vocab-index.js — do not edit manually
export const vocabMap = {
${lines.join(',\n')}
};
`;

fs.writeFileSync(OUTPUT_FILE, output, 'utf-8');
console.log(`Wrote ${keys.length} entries to ${OUTPUT_FILE}`);
